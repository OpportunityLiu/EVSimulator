public final class LongLat
{
    /**
     * 经度
     */
    public final double longitude;
    /**
     * 纬度
     */
    public final double latitude;

    public LongLat(double longitude, double latitude)
    {
        this.longitude = longitude;
        this.latitude = latitude;
    }
}
